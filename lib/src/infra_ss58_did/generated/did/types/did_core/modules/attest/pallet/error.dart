import 'dart:typed_data' as _i2;

import 'package:polkadart/scale_codec.dart' as _i1;

/// Error for the attest module.
enum Error {
  /// The Attestation was not posted because its priority was less than or equal to that of
  /// an attestation previously posted by the same entity.
  ///
  /// Note, the default value for an attestation is the null claim (claiming nothing), with
  /// a priority of 0. To override this initial value, a priority of 1 or greater is
  /// required. Check to see that the provided priority is not zero as that could be the
  /// cause of this error.
  priorityTooLow('PriorityTooLow', 0);

  const Error(
    this.variantName,
    this.codecIndex,
  );

  factory Error.decode(_i1.Input input) {
    return codec.decode(input);
  }

  final String variantName;

  final int codecIndex;

  static const $ErrorCodec codec = $ErrorCodec();

  String toJson() => variantName;
  _i2.Uint8List encode() {
    return codec.encode(this);
  }
}

class $ErrorCodec with _i1.Codec<Error> {
  const $ErrorCodec();

  @override
  Error decode(_i1.Input input) {
    final index = _i1.U8Codec.codec.decode(input);
    switch (index) {
      case 0:
        return Error.priorityTooLow;
      default:
        throw Exception('Error: Invalid variant index: "$index"');
    }
  }

  @override
  void encodeTo(
    Error value,
    _i1.Output output,
  ) {
    _i1.U8Codec.codec.encodeTo(
      value.codecIndex,
      output,
    );
  }
}
